import UIKit

extension UIColor {
	
	/// VC主背景颜色
	class func themeBackgroundColor() -> UIColor{
		return UIColor(red: 39 / 255.0, green: 150 / 255.0, blue: 255 / 255.0, alpha: 1.0);
	}
	
}
